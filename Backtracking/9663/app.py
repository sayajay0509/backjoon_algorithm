def dfs(n):
    global ans
    if n == N:
        ans += 1
        return
    for j in range(N):
        if v1[j] == 0 and v2[n + j] == 0 and v3[n - j + N] == 0:  # 조건 체크
            v1[j] = v2[n + j] = v3[n - j + N] = 1  # 퀸을 놓습니다.
            dfs(n + 1)
            v1[j] = v2[n + j] = v3[n - j + N] = 0  # 퀸을 제거합니다.

N = int(input())
ans = 0
v1 = [0] * N  # 각 열에 퀸이 있는지 여부
v2 = [0] * (2 * N)  # 주 대각선에 퀸이 있는지 여부
v3 = [0] * (2 * N)  # 부 대각선에 퀸이 있는지 여부
dfs(0)
print(ans)